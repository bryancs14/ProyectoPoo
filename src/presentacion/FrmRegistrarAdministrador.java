    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection cn = null;
        PreparedStatement ps = null;
        ResultSet rs;
        String idAdministrador = txtIdAdministrador.getText();
        String nombre = txtNombre.getText();
        String contraseña = String.valueOf(txtContraseña.getPassword());
        String sql = "select * from administrador where idAdministrador = ? and nombre = ? and contraseña = ?";
        try {
            cn = Conexion.getInstancia().miConexion();
            ps = cn.prepareStatement(sql);

            ps.setString(1, idAdministrador);
            ps.setString(2, nombre);
            ps.setString(3, contraseña);

            rs = ps.executeQuery();

            if(rs.next()){
                FrmPrincipal x = new FrmPrincipal();
                x.setVisible(true);
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "DATOS INCORRECTOS");
            }

        }catch(SQLException e) {

        }

    }//GEN-LAST:event_jButton1ActionPerformed

